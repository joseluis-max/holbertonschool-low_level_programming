#include "3-calc.h"
/**
 * op_add - add two int
 * @a: integer
 * @b: integer
 * Return: sum of a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub two int
 * @a: integer
 * @b: integer
 * Return: sub of a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mul two int
 * @a: integer
 * @b: integer
 * Return: sum of a * b
 */
int op_mul(int a, int b)
{
        return (a * b);
}
/**
 * op_div - div two int
 * @a: integer
 * @b: integer
 * Return: div of a / b
 */
int op_div(int a, int b)
{
        return (a / b);
}
/**
 * op_mod - mod two int
 * @a: integer
 * @b: integer
 * Return: sum of a % b
 */
int op_mod(int a, int b)
{
        return (a % b);
}



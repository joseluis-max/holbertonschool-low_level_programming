/**
 * _sqrt_recursion - natural square root
 * @n: number integer
 * Return: natural square root or -1
 */
int counter(int i, int n)
{
	if (i * i == n)
		return (i)
	i++;
}
int _sqrt_recursion(int n)
{
	if (n < 0)
	{
		return (-1);
	}
	if (n == 0)
	{
		return (0);
	}
	counter(0, n);
	return (_sqrt_recursion(n));
}
